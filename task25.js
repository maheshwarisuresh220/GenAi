"use strict";
/* Stages of Life*/
let age = 2;
if (age <= 2) {
    console.log('User is baby');
}
else if (age >= 2 && age <= 4) {
    console.log('User is toddler');
}
else if (age >= 4 && age <= 13) {
    console.log('User is Kid');
}
else if (age >= 13 && age <= 20) {
    console.log('User is Teenager');
}
else if (age >= 20 && age <= 65) {
    console.log('User is an adult');
}
else {
    console.log('User is elder');
}
